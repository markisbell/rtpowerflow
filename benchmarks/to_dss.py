"""Exporter: a frozen fixture -> an OpenDSS model (docs/BENCHMARKS.md §5).

Reads the fixture through netzsim's real pipeline (so the EFFECTIVE
pandapower tables are mapped, std-types resolved) and writes
``benchmarks/out/dss/<fixture>/circuit.dss`` + per-element loadshape CSVs
+ ``mapping.json`` (bus/line name -> pandapower index) + a human-readable
``mapping_report.txt`` for external audit.

Model-alignment choices (every one documented in the plan):
- balanced 3-phase from sequence data, r0/x0/c0 = r1/x1/c1
- ideal slack (Model=Ideal, puZideal 1e-8) at the ext_grid bus
- 50 Hz set BEFORE New Circuit (C1->B conversion!)
- loads/sgens as constant-PQ Loads with Vminpu=0 and ABSOLUTE daily
  shapes (LoadShape UseActual=yes, Pmult/Qmult in kW/kvar) — sgens are
  negative loads, which sidesteps P/Q-proportionality assumptions
- transformer: FULL mapping incl. magnetizing branch (XHL, %loadloss,
  %noloadloss, %imag), wye-wye grounded, ppm_antifloat=0 — the vector
  group shift (Dyn) is dropped, so compare magnitudes only
"""
from __future__ import annotations

import argparse
import json
import math
import sys
from pathlib import Path

import numpy as np

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))

from netzsim.data_loader import load_inputs   # noqa: E402
from netzsim.simulator import Simulator       # noqa: E402


def export(fixture: str, align_trafo: bool = False) -> Path:
    fdir = ROOT / "benchmarks" / "fixtures" / fixture
    data = load_inputs(fdir)
    sim = Simulator(data)
    net = sim.net
    steps = sim.steps_per_day

    suffix = "_aligned" if align_trafo else ""
    out = ROOT / "benchmarks" / "out" / "dss" / (fixture + suffix)
    shapes = out / "shapes"
    shapes.mkdir(parents=True, exist_ok=True)

    # -- sanity of unsupported corners ------------------------------------ #
    assert (net.line.g_us_per_km.fillna(0) == 0).all(), "line shunt G not mapped"
    if len(net.trafo):
        tap_neutral = net.trafo.tap_neutral.fillna(0)
        tap_pos = net.trafo.tap_pos.fillna(tap_neutral)
        assert (tap_pos == tap_neutral).all(), "off-neutral taps not mapped"
    slack_vm = sim.prof.ext_vm
    assert np.allclose(slack_vm, slack_vm[:, :1]), "varying slack profile"

    L: list[str] = []
    L.append(f"! generated by benchmarks/to_dss.py from fixture {fixture}")
    L.append("Clear")
    L.append("Set DefaultBaseFrequency=50")

    # -- ideal slack -------------------------------------------------------- #
    eg = net.ext_grid.iloc[0]
    slack_bus = int(eg.bus)
    vn_slack = float(net.bus.at[slack_bus, "vn_kv"])
    vm_slack = float(slack_vm[0, 0])
    L.append(f"New Circuit.{fixture} bus1=b{slack_bus} basekv={vn_slack:.6g} "
             f"pu={vm_slack:.6g} phases=3 Model=Ideal puZideal=[1e-8, 1e-8]")

    # -- lines: one linecode per line, everything explicit. pandapower's
    # `parallel` divides the series impedance and multiplies the shunt —
    # fold it into EFFECTIVE per-km values so OpenDSS sees one equivalent
    # conductor (netzsim's i_ka is the summed current of the bundle).
    for i, ln in net.line.iterrows():
        par = float(ln.parallel)
        L.append(
            f"New LineCode.lc_{i} nphases=3 units=km "
            f"r1={ln.r_ohm_per_km / par:.9g} x1={ln.x_ohm_per_km / par:.9g} "
            f"c1={ln.c_nf_per_km * par:.9g} "
            f"r0={ln.r_ohm_per_km / par:.9g} x0={ln.x_ohm_per_km / par:.9g} "
            f"c0={ln.c_nf_per_km * par:.9g} "
            f"normamps={ln.max_i_ka * 1000 * par:.6g}")
        enabled = "yes" if bool(ln.in_service) else "no"
        L.append(
            f"New Line.line_{i} bus1=b{int(ln.from_bus)} bus2=b{int(ln.to_bus)} "
            f"linecode=lc_{i} length={ln.length_km:.9g} units=km phases=3 "
            f"enabled={enabled}")

    # -- transformers: full mapping incl. magnetizing branch ---------------- #
    for i, tr in net.trafo.iterrows():
        xhl = math.sqrt(max(tr.vk_percent**2 - tr.vkr_percent**2, 0.0))
        if align_trafo:
            noload = imag = 0.0     # plan §5.2 model alignment (T ≡ series)
        else:
            noload = float(tr.pfe_kw) / (10.0 * tr.sn_mva) if tr.sn_mva else 0.0
            imag = math.sqrt(max(float(tr.i0_percent)**2 - noload**2, 0.0))
        kvas = tr.sn_mva * 1000.0
        L.append(
            f"New Transformer.trafo_{i} phases=3 windings=2 "
            f"buses=[b{int(tr.hv_bus)}.1.2.3.0 b{int(tr.lv_bus)}.1.2.3.0] "
            f"conns=[wye wye] kVs=[{tr.vn_hv_kv:.6g} {tr.vn_lv_kv:.6g}] "
            f"kVAs=[{kvas:.6g} {kvas:.6g}] XHL={xhl:.9g} "
            f"%loadloss={tr.vkr_percent:.9g} %noloadloss={noload:.9g} "
            f"%imag={imag:.9g} ppm_antifloat=0")

    # -- loads + sgens as constant-PQ loads with ABSOLUTE daily shapes ------ #
    def add_pq(kind: str, i: int, li: int, bus: int, p_mw, q_mvar) -> None:
        sign = -1.0 if kind == "gen" else 1.0
        np.savetxt(shapes / f"{kind}_p_{i}.csv", sign * p_mw * 1000.0,
                   fmt="%.6f")
        np.savetxt(shapes / f"{kind}_q_{i}.csv", sign * q_mvar * 1000.0,
                   fmt="%.6f")
        vn = float(net.bus.at[bus, "vn_kv"])
        L.append(
            f"New LoadShape.ls_{kind}_{i} npts={steps} minterval=1 "
            f"UseActual=yes "
            f"Pmult=(file=shapes/{kind}_p_{i}.csv) "
            f"Qmult=(file=shapes/{kind}_q_{i}.csv)")
        L.append(
            f"New Load.{kind}_{i} bus1=b{bus} phases=3 conn=wye model=1 "
            f"kv={vn:.6g} kW=1 kvar=0 Vminpu=0.0 Vmaxpu=2.0 "
            f"daily=ls_{kind}_{i}")

    for i, li in enumerate(sim.prof.load_idx):
        add_pq("load", i, li, int(net.load.at[li, "bus"]),
               sim.prof.load_p[i], sim.prof.load_q[i])
    for i, si in enumerate(sim.prof.sgen_idx):
        add_pq("gen", i, si, int(net.sgen.at[si, "bus"]),
               sim.prof.sgen_p[i], sim.prof.sgen_q[i])

    # -- solver setup -------------------------------------------------------- #
    vbases = sorted(set(round(float(v), 6) for v in net.bus.vn_kv))
    L.append(f"Set VoltageBases=[{', '.join(f'{v:g}' for v in vbases)}]")
    L.append("CalcVoltageBases")
    L.append("Set tolerance=1e-8")
    L.append("Set maxiterations=100")
    L.append("Set ControlMode=Static")

    (out / "circuit.dss").write_text("\n".join(L) + "\n", encoding="ascii")
    (out / "mapping.json").write_text(json.dumps({
        "steps": steps,
        "buses": {f"b{int(b)}": int(b) for b in net.bus.index},
        "vn_kv": {f"b{int(b)}": float(net.bus.at[b, 'vn_kv'])
                  for b in net.bus.index},
        "lines": {f"line_{int(i)}": int(i) for i in net.line.index},
        "slack_bus": slack_bus,
    }), encoding="ascii")
    (out / "mapping_report.txt").write_text(
        f"fixture {fixture}: {len(net.bus)} buses, {len(net.line)} lines "
        f"({int((~net.line.in_service).sum())} normally open), "
        f"{len(net.trafo)} trafos, {len(sim.prof.load_idx)} loads, "
        f"{len(sim.prof.sgen_idx)} sgens as negative loads\n"
        f"slack: bus {slack_bus} @ {vm_slack} pu (Model=Ideal)\n"
        f"voltage bases (kV LL): {vbases}\n"
        f"alignment: 50 Hz; r0/x0/c0=r1/x1/c1; wye-wye grounded trafos "
        f"(vector-group shift dropped -> magnitude comparison); "
        f"magnetizing branch mapped (not zeroed); Vminpu=0 on all loads; "
        f"tolerance 1e-8\n", encoding="ascii")
    print(f"{fixture}: wrote {out / 'circuit.dss'} "
          f"({len(net.bus)} buses, {len(net.line)} lines, "
          f"{len(sim.prof.load_idx)}+{len(sim.prof.sgen_idx)} PQ elements)")
    return out


if __name__ == "__main__":
    ap = argparse.ArgumentParser()
    ap.add_argument("fixture")
    ap.add_argument("--align-trafo", action="store_true",
                    help="zero the trafo magnetizing branch (plan §5.2)")
    a = ap.parse_args()
    export(a.fixture, a.align_trafo)
