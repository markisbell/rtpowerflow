"""Generate the public validation report (docs/benchmarks/) from the
artifacts under benchmarks/out/ — idempotent, rerun after any benchmark.

Writes:
    docs/benchmarks/manifest.json   engine versions + fixture hashes + results
    docs/benchmarks/README.md       the report (method, tables, figures)
    docs/benchmarks/data/*.csv      drill-down copies of the result tables
"""
from __future__ import annotations

import hashlib
import json
import platform
import shutil
import subprocess
import sys
import time
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "benchmarks" / "out"
DOCS = ROOT / "docs" / "benchmarks"

G_LABELS = {
    "g1_lv_rural": "G1 rural LV — scenario 1 (75-kWp farm PV), 30 buses",
    "g2_lv_suburban": "G2 suburban LV — scenario 2 (EV evening), 62 buses",
    "g3_mv_district": "G3 MV district — 475 buses incl. 110-kV feed",
}


def _versions() -> dict:
    import pandapower
    from opendssdirect import dss
    info = {
        "python": sys.version.split()[0],
        "platform": platform.platform(),
        "pandapower": pandapower.__version__,
        "opendssdirect": f"{dss.__version__} / engine "
                         f"{dss.Basic.Version().split(';')[0].strip()}",
    }
    try:
        import py_dss_interface
        d = py_dss_interface.DSS()
        info["epri_engine"] = str(d.dssinterface.version).strip()
    except Exception as exc:  # noqa: BLE001
        info["epri_engine"] = f"unavailable ({exc})"
    info["matpower"] = "MATPOWER 8.1 via matpower-pip 8.1.0.2.3.0 " \
                       "on GNU Octave 11.3.0"
    try:
        info["netzsim_commit"] = subprocess.run(
            ["git", "log", "-1", "--format=%h %s"], cwd=ROOT,
            capture_output=True, text=True, check=True).stdout.strip()
    except Exception:  # noqa: BLE001
        info["netzsim_commit"] = "unknown"
    return info


def main() -> int:
    DOCS.mkdir(parents=True, exist_ok=True)
    (DOCS / "data").mkdir(exist_ok=True)

    t_rows = json.loads((OUT / "t_series.json").read_text(encoding="utf-8"))
    shutil.copy(OUT / "t_series.csv", DOCS / "data" / "t_series.csv")

    g_rows = []
    for f in sorted(OUT.glob("*_compare_*.json")):
        g_rows.append(json.loads(f.read_text(encoding="utf-8")))
        shutil.copy(f, DOCS / "data" / f.name)

    fixture_hashes = {}
    for fdir in sorted((ROOT / "benchmarks" / "fixtures").glob("g*")):
        h = hashlib.sha256()
        for p in sorted(fdir.glob("*.json")):
            h.update(p.read_bytes())
        fixture_hashes[fdir.name] = h.hexdigest()[:16]

    manifest = {"generated": time.strftime("%Y-%m-%dT%H:%M:%S"),
                "environment": _versions(),
                "fixtures_sha256_16": fixture_hashes,
                "t_series": t_rows, "g_series": g_rows}
    (DOCS / "manifest.json").write_text(json.dumps(manifest, indent=2),
                                        encoding="utf-8")

    # ---------------- README ------------------------------------------- #
    L = []
    L.append("# Validation: netzsim vs OpenDSS and MATPOWER\n")
    L.append("> Generated by `benchmarks/make_report.py` — regenerate with "
             "`python benchmarks/run_all.py` (method & full plan: "
             "[`docs/BENCHMARKS.md`](../BENCHMARKS.md); exact engine "
             "versions: [`manifest.json`](manifest.json)). The source code "
             "of this repository is AI-generated (see the main README); "
             "this validation exists precisely so you don't have to take "
             "its word for it.\n")
    L.append("## What is compared\n")
    L.append(
        "netzsim's full pipeline (input JSONs → network construction → "
        "1440-step day) against two independent solvers on **balanced "
        "positive-sequence** models: **OpenDSS** in native daily mode "
        "(AltDSS engine — dss-extensions' regularly cross-validated "
        "implementation; the official EPRI 11.0.0.1 engine is installed "
        "and verified loadable, its scripted leg is a documented open "
        "item) and **real MATPOWER 8.1** on GNU Octave. Model alignment "
        "(fully disclosed): ideal slack, 50 Hz, `Vminpu=0`, wye-wye "
        "transformers with the magnetizing branch zeroed on both sides "
        "(plan §5.2) — the full-model supplementary runs quantify that "
        "alignment at ~2–3e-5 pu (the magnetizing-branch *position* "
        "differs between pandapower's T model and OpenDSS). Inputs are "
        "frozen fixtures (`benchmarks/fixtures/`, seeds baked in, "
        "SHA-256 in the manifest).\n")

    L.append("## T-series: IEEE cases, pandapower vs MATPOWER 8.1 "
             "(byte-identical case data)\n")
    L.append("| id | case | buses | max \\|ΔVm\\| [pu] | max \\|ΔVa\\| [deg] "
             "| gate | result |")
    L.append("|---|---|---|---|---|---|---|")
    for r in t_rows:
        L.append(f"| {r['id']} | {r['case']} | {r['n_bus']} "
                 f"| {r['max_dvm_pu']} | {r['max_dva_deg']} "
                 f"| 1e-6 pu | {'✅ PASS' if r['pass'] else '❌ FAIL'} |")
    L.append("")

    L.append("## G-series: the committed teaching grids over the full "
             "1440-step day\n")
    L.append("| grid | vs | model | max \\|ΔV\\| [pu] | mean \\|ΔV\\| [pu] "
             "| max \\|ΔI\\| [A] | gate | result |")
    L.append("|---|---|---|---|---|---|---|---|")
    for r in sorted(g_rows, key=lambda x: (x["fixture"], x["engine"],
                                           x.get("trafo_model", ""))):
        label = G_LABELS.get(r["fixture"], r["fixture"])
        eng = ("OpenDSS (AltDSS)" if r["engine"] == "altdss"
               else "MATPOWER 8.1" if "matpower" in r["engine"]
               else r["engine"])
        model = ("aligned" if "aligned" in r.get("trafo_model", "")
                 else "full model")
        di = f"{r['max_di_a']:.4f}" if "max_di_a" in r else "—"
        gate = f"{r['gate_dv_pu']:g} pu"
        verdict = ("✅ PASS" if r["pass"]
                   else "ℹ️ supplementary" if model == "full model"
                   else "❌ FAIL")
        L.append(f"| {label} | {eng} | {model} | {r['max_dv_pu']:.3e} "
                 f"| {r['mean_dv_pu']:.3e} | {di} | {gate} | {verdict} |")
    L.append("")
    L.append("The *full model* rows keep the transformer magnetizing branch "
             "on both sides and measure the shunt-**position** difference "
             "between the tools — they are reported for transparency, the "
             "*aligned* rows are the pass/fail gate runs.\n")

    L.append("## Daily profiles (netzsim line, OpenDSS dots, Δ below)\n")
    for img, cap in (
            ("g1_farm_voltage.png",
             "G1: the scenario-1 story — 75-kWp PV lifts the farm bus to "
             "≈437 V L-L (252 V L-N) at noon; both tools agree to ±0.2 mV."),
            ("g1_head_current.png", "G1 feeder head current."),
            ("g2_weak_voltage.png",
             "G2: the EV-evening dip at the weakest bus."),
            ("g2_head_current.png",
             "G2 feeder head current — the NH-fuse plateau of scenario 2."),
            ("g3_worst_voltage.png",
             "G3: the lowest MV bus of the 475-bus district (Δ in mV at "
             "10 kV)."),
            ("error_over_day.png",
             "Max voltage error across all buses per step — the agreement "
             "holds through PV noon and EV evening, orders below the "
             "1e-5-pu gate.")):
        L.append(f"![{img}](img/{img})\n\n*{cap}*\n")

    L.append("## Reproduce\n")
    L.append("```\npy -3.12 -m venv .venv-bench\n"
             ".venv-bench\\Scripts\\pip install -r benchmarks\\requirements.txt\n"
             "# GNU Octave 11.3.0 + set OCTAVE_EXECUTABLE (see docs/BENCHMARKS.md §3.2)\n"
             ".venv-bench\\Scripts\\python benchmarks\\run_all.py\n```\n")
    L.append("Known limitations: balanced positive-sequence only (the "
             "unbalanced IEEE feeders are out of scope by design); the "
             "±0.2-mV noise floor is the 1-W rounding of the frozen fixture "
             "profiles; vector-group angle shifts are dropped (magnitude "
             "comparison).\n")

    (DOCS / "README.md").write_text("\n".join(L), encoding="utf-8")
    print(f"wrote {DOCS / 'README.md'} + manifest.json "
          f"({len(t_rows)} T rows, {len(g_rows)} G rows)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
